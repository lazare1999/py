# ფუნქციისათვის არგუმენტად გადაცემული სია : [1,2,3,3,3,3,4,5]
# ფუნქციის მიერ დაბრუნებული სია : [1, 2, 3, 4, 5]

def unics(list):
    temp_list = []
    for item in list:
        if item in temp_list:
            continue
        else:
            temp_list.append(item)
    print(temp_list)


unics([1, 2, 3, 3, 3, 3, 4, 5])
